# COMANDOS PARA COLOCAR O CÓDIGO NO GITHUB #
# Abrir a pasta do projeto no vscode e abrir o terminal
# Inicializar o repositório git = git init
# Adicionar todos os arquivos = git add . ou git add "nome do arquivo"
# Confirmar alterações = git commit -m "Nome do commit"
# Adicionar a URL do repositório = git remote add origin <URL_DO_REPOSITÓRIO>
# Enviar as alterações para o github = git push -u origin master


